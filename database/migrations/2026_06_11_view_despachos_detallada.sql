        ELSE NULL
    END AS TIPO_EMBARQUE,
    COALESCE(des.NAVE_DESPACHOEX, ic.NAVE_ICARGA) AS NAVE,
    COALESCE(des.NVIAJE_DESPACHOEX, ic.NVIAJE_ICARGA) AS NUMERO_VIAJE,
    COALESCE(des.FECHAETD_DESPACHOEX, ic.FECHAETD_ICARGA) AS FECHA_ETD,
    COALESCE(des.FECHAETA_DESPACHOEX, ic.FECHAETA_ICARGA) AS FECHA_ETA,
    ic.FECHAETDREAL_ICARGA AS FECHA_REAL_ETD,
    ic.FECHAETAREAL_ICARGA AS FECHA_REAL_ETA,
    des.TERMOGRAFO_DESPACHOEX AS TERMOGRAFO_DESPACHO,
    exi.N_TERMOGRAFO AS TERMOGRAFO_PALLET,
    des.ID_EMPRESA,
    des.ID_PLANTA,
    des.ID_TEMPORADA,
    esp.ID_ESPECIES,
    exi.ID_VESPECIES,
    exi.ID_PRODUCTOR,
    exi.ID_ICARGA
FROM fruta_exiexportacion exi
INNER JOIN fruta_despachoex des ON des.ID_DESPACHOEX = exi.ID_DESPACHOEX
LEFT JOIN (
    SELECT
        ID_EXIEXPORTACION,
        MAX(FOB_REFERENCIA_CAJA) AS FOB_REFERENCIA_CAJA,
        MAX(FOB_REFERENCIA_KG) AS FOB_REFERENCIA_KG,
        MAX(FOB_VENTA_CAJA) AS FOB_VENTA_CAJA,
        MAX(FOB_VENTA_KG) AS FOB_VENTA_KG,
        SUM(VENTA_USD) AS VENTA_USD,
        SUM(VENTA_USD_BRUTO) AS VENTA_USD_BRUTO,
        SUM(VENTA_USD_NETO) AS VENTA_USD_NETO,
        SUM(RETORNO_NETO) AS RETORNO_NETO,
        MAX(FOB_FINAL_KG) AS FOB_FINAL_KG,
        MAX(FOB_FINAL_CAJA) AS FOB_FINAL_CAJA,
        MAX(NUMERO_VALOR) AS NUMERO_VALOR,
        MAX(FECHA_VALOR) AS FECHA_VALOR,
        MAX(ESTADO_LIQUIDACION) AS ESTADO_LIQUIDACION
    FROM view_liquidacion_exportacion
    GROUP BY ID_EXIEXPORTACION
) liq ON liq.ID_EXIEXPORTACION = exi.ID_EXIEXPORTACION
LEFT JOIN (
    SELECT
        inv.ID_ICARGA,
        det.ID_ESTANDAR,
        det.ID_VESPECIES,
        det.ID_TCALIBRE,
        MAX(det.PRECIO_CAJA) AS PRECIO_CAJA_INVOICE,
        SUM(det.TOTAL_LINEA) AS TOTAL_LINEA_INVOICE,
        MAX(inv.NUMERO_INVOICE) AS NUMERO_INVOICE,
        MAX(inv.MONEDA_INVOICE) AS MONEDA_INVOICE
    FROM exportadora_invoice_detalle det
    INNER JOIN exportadora_invoice inv ON inv.ID_INVOICE = det.ID_INVOICE
    WHERE det.ESTADO_REGISTRO = 1
      AND inv.ESTADO_REGISTRO = 1
    GROUP BY inv.ID_ICARGA, det.ID_ESTANDAR, det.ID_VESPECIES, det.ID_TCALIBRE
) inv ON inv.ID_ICARGA = exi.ID_ICARGA
     AND inv.ID_ESTANDAR = exi.ID_ESTANDAR
     AND inv.ID_VESPECIES = exi.ID_VESPECIES
     AND inv.ID_TCALIBRE = exi.ID_TCALIBRE
LEFT JOIN fruta_icarga ic ON ic.ID_ICARGA = exi.ID_ICARGA
LEFT JOIN estandar_eexportacion est ON est.ID_ESTANDAR = exi.ID_ESTANDAR
LEFT JOIN fruta_productor prod ON prod.ID_PRODUCTOR = exi.ID_PRODUCTOR
LEFT JOIN fruta_vespecies vesp ON vesp.ID_VESPECIES = exi.ID_VESPECIES
LEFT JOIN fruta_especies esp ON esp.ID_ESPECIES = vesp.ID_ESPECIES
LEFT JOIN fruta_tmanejo tm ON tm.ID_TMANEJO = exi.ID_TMANEJO
LEFT JOIN fruta_tcalibre tc ON tc.ID_TCALIBRE = exi.ID_TCALIBRE
LEFT JOIN fruta_tembalaje tem ON tem.ID_TEMBALAJE = exi.ID_TEMBALAJE
LEFT JOIN transporte_transporte trans ON trans.ID_TRANSPORTE = des.ID_TRANSPORTE
LEFT JOIN transporte_conductor cond ON cond.ID_CONDUCTOR = des.ID_CONDUCTOR
LEFT JOIN principal_empresa emp ON emp.ID_EMPRESA = des.ID_EMPRESA
LEFT JOIN principal_planta planta ON planta.ID_PLANTA = des.ID_PLANTA
LEFT JOIN principal_temporada temp ON temp.ID_TEMPORADA = des.ID_TEMPORADA
LEFT JOIN fruta_dfinal dfinal ON dfinal.ID_DFINAL = des.ID_DFINAL
LEFT JOIN fruta_mercado mercado ON mercado.ID_MERCADO = COALESCE(ic.ID_MERCADO, des.ID_MERCADO)
LEFT JOIN fruta_broker broker ON broker.ID_BROKER = ic.ID_BROKER
LEFT JOIN fruta_rfinal rfinal ON rfinal.ID_RFINAL = COALESCE(des.ID_RFINAL, ic.ID_RFINAL)
WHERE exi.ESTADO_REGISTRO = 1
  AND exi.ESTADO IN (7, 8)
  AND des.ESTADO_REGISTRO = 1
  AND des.ID_EMPRESA <> 5;
