        WHEN 1 THEN 'Interplanta'
        WHEN 2 THEN 'Devolucion Productor'
        WHEN 3 THEN 'Venta'
        WHEN 4 THEN 'Despacho de Descarte'
        WHEN 5 THEN 'Planta Externa'
