    NULL AS FOB_FINAL_CAJA,
    NULL AS NUMERO_VALOR,
    NULL AS FECHA_VALOR,
    NULL AS ESTADO_LIQUIDACION,
    NULL AS PRECIO_CAJA_INVOICE,
    NULL AS TOTAL_LINEA_INVOICE,
    NULL AS NUMERO_INVOICE,
    NULL AS MONEDA_INVOICE,
    NULL AS NREFERENCIA_ICARGA,
    NULL AS CLIENTE,
    NULL AS MERCADO,
    NULL AS CONTENEDOR,
    NULL AS BL_AWB,
    NULL AS RECIBIDOR_FINAL,
    NULL AS TIPO_EMBARQUE,
    NULL AS NAVE,
    NULL AS NUMERO_VIAJE,
    NULL AS FECHA_ETD,
    NULL AS FECHA_ETA,
    NULL AS FECHA_REAL_ETD,
    NULL AS FECHA_REAL_ETA,
    NULL AS TERMOGRAFO_DESPACHO,
    NULL AS TERMOGRAFO_PALLET,
