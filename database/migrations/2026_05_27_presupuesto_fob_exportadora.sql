CREATE TABLE IF NOT EXISTS exportadora_presupuesto_fob (
    ID_PRESUPUESTO_FOB BIGINT(20) NOT NULL AUTO_INCREMENT,
    ID_TEMPORADA BIGINT(20) NOT NULL,
    ID_MERCADO BIGINT(20) NOT NULL,
    ID_CONSIGNATARIO BIGINT(20) NOT NULL,
    ID_ESTANDAR BIGINT(20) NOT NULL,
    ID_TCALIBRE BIGINT(20) NOT NULL,
    ID_TMONEDA BIGINT(20) DEFAULT NULL,
    ID_CVENTA BIGINT(20) DEFAULT NULL,
    ID_MVENTA BIGINT(20) DEFAULT NULL,
    ID_TFLETE BIGINT(20) DEFAULT NULL,
    PESO_CAJA DECIMAL(12,4) DEFAULT NULL,
    FOB_CAJA DECIMAL(12,4) NOT NULL DEFAULT 0,
    MOTIVO_AJUSTE VARCHAR(300) DEFAULT NULL,
    ESTADO_REGISTRO INT(11) NOT NULL DEFAULT 1,
    INGRESO DATETIME DEFAULT NULL,
    MODIFICACION DATETIME DEFAULT NULL,
    ID_EMPRESA BIGINT(20) NOT NULL,
    ID_USUARIOI BIGINT(20) NOT NULL,
    ID_USUARIOM BIGINT(20) NOT NULL,
    PRIMARY KEY (ID_PRESUPUESTO_FOB),
    KEY IDX_PRESUPUESTO_FOB_BUSQUEDA (ID_EMPRESA, ID_TEMPORADA, ID_MERCADO, ID_CONSIGNATARIO, ID_ESTANDAR, ID_TCALIBRE, ESTADO_REGISTRO),
    KEY IDX_PRESUPUESTO_FOB_COMERCIAL (ID_EMPRESA, ID_TEMPORADA, ID_MERCADO, ID_CONSIGNATARIO, ESTADO_REGISTRO)
);

CREATE TABLE IF NOT EXISTS exportadora_presupuesto_fob_historial (
    ID_HISTORIAL_FOB BIGINT(20) NOT NULL AUTO_INCREMENT,
    ID_PRESUPUESTO_FOB BIGINT(20) NOT NULL,
    FOB_CAJA_ANTERIOR DECIMAL(12,4) DEFAULT NULL,
    FOB_CAJA_NUEVO DECIMAL(12,4) DEFAULT NULL,
    PESO_CAJA_ANTERIOR DECIMAL(12,4) DEFAULT NULL,
    PESO_CAJA_NUEVO DECIMAL(12,4) DEFAULT NULL,
    ID_TMONEDA_ANTERIOR BIGINT(20) DEFAULT NULL,
    ID_TMONEDA_NUEVO BIGINT(20) DEFAULT NULL,
    ID_CVENTA_ANTERIOR BIGINT(20) DEFAULT NULL,
    ID_CVENTA_NUEVO BIGINT(20) DEFAULT NULL,
    ID_MVENTA_ANTERIOR BIGINT(20) DEFAULT NULL,
    ID_MVENTA_NUEVO BIGINT(20) DEFAULT NULL,
    ID_TFLETE_ANTERIOR BIGINT(20) DEFAULT NULL,
    ID_TFLETE_NUEVO BIGINT(20) DEFAULT NULL,
    MOTIVO_AJUSTE VARCHAR(300) DEFAULT NULL,
    INGRESO DATETIME DEFAULT NULL,
    ID_USUARIO BIGINT(20) NOT NULL,
    PRIMARY KEY (ID_HISTORIAL_FOB),
    KEY IDX_HISTORIAL_FOB_PRESUPUESTO (ID_PRESUPUESTO_FOB)
);
