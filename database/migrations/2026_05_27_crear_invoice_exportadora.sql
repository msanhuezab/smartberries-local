CREATE TABLE IF NOT EXISTS exportadora_invoice (
    ID_INVOICE BIGINT NOT NULL AUTO_INCREMENT,
    ID_ICARGA BIGINT NOT NULL,
    NUMERO_INVOICE INT DEFAULT NULL,
    FECHA_INVOICE DATE DEFAULT NULL,
    MONEDA_INVOICE VARCHAR(3) NOT NULL DEFAULT 'USD',
    TIPO_CAMBIO_USD DECIMAL(14,6) NOT NULL DEFAULT 1,
    ESTADO_INVOICE VARCHAR(20) NOT NULL DEFAULT 'BORRADOR',
    OBSERVACION_INVOICE VARCHAR(500) DEFAULT NULL,
    ESTADO_REGISTRO INT DEFAULT 1,
    INGRESO DATETIME DEFAULT NULL,
    MODIFICACION DATETIME DEFAULT NULL,
    ID_EMPRESA BIGINT NOT NULL,
    ID_TEMPORADA BIGINT NOT NULL,
    ID_USUARIOI BIGINT NOT NULL,
    ID_USUARIOM BIGINT NOT NULL,
    PRIMARY KEY (ID_INVOICE),
    KEY idx_invoice_icarga (ID_ICARGA, ESTADO_REGISTRO),
    KEY idx_invoice_empresa_temporada (ID_EMPRESA, ID_TEMPORADA)
);

CREATE TABLE IF NOT EXISTS exportadora_invoice_detalle (
    ID_DINVOICE BIGINT NOT NULL AUTO_INCREMENT,
    ID_INVOICE BIGINT NOT NULL,
    ID_DICARGA BIGINT DEFAULT 0,
    ID_ESTANDAR BIGINT NOT NULL DEFAULT 0,
    ID_VESPECIES BIGINT NOT NULL DEFAULT 0,
    ID_TCALIBRE BIGINT NOT NULL DEFAULT 0,
    CANTIDAD_ENVASE DECIMAL(14,2) NOT NULL DEFAULT 0,
    KILOS_NETO DECIMAL(14,2) NOT NULL DEFAULT 0,
    KILOS_BRUTO DECIMAL(14,2) NOT NULL DEFAULT 0,
    PRECIO_CAJA DECIMAL(14,4) NOT NULL DEFAULT 0,
    TOTAL_LINEA DECIMAL(14,2) NOT NULL DEFAULT 0,
    ORIGEN VARCHAR(20) NOT NULL DEFAULT 'AUTO',
    OBSERVACION VARCHAR(300) DEFAULT NULL,
    ESTADO_REGISTRO INT DEFAULT 1,
    INGRESO DATETIME DEFAULT NULL,
    MODIFICACION DATETIME DEFAULT NULL,
    ID_USUARIOI BIGINT NOT NULL,
    ID_USUARIOM BIGINT NOT NULL,
    PRIMARY KEY (ID_DINVOICE),
    UNIQUE KEY uq_invoice_linea (ID_INVOICE, ID_DICARGA, ID_ESTANDAR, ID_VESPECIES, ID_TCALIBRE),
    KEY idx_dinvoice_dimensiones (ID_ESTANDAR, ID_VESPECIES, ID_TCALIBRE)
);
