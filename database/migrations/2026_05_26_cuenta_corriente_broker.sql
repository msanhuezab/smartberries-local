CREATE TABLE IF NOT EXISTS cuenta_corriente_broker (
    ID_CCBROKER      BIGINT        NOT NULL AUTO_INCREMENT PRIMARY KEY,
    ID_BROKER        BIGINT        NOT NULL,
    FECHA_MOVIMIENTO DATE          NOT NULL,
    TIPO_MOVIMIENTO  ENUM('DEBE','HABER') NOT NULL,
    CONCEPTO         VARCHAR(300)  NOT NULL,
    MONTO_USD        DECIMAL(14,2) NOT NULL DEFAULT 0,
    MONEDA_ORIGEN    VARCHAR(3)    NOT NULL DEFAULT 'USD',
    TIPO_CAMBIO_USD  DECIMAL(14,6) NOT NULL DEFAULT 1.000000,
    REFERENCIA       VARCHAR(100)  NULL,
    ESTADO_REGISTRO  TINYINT       NOT NULL DEFAULT 1,
    INGRESO          DATETIME      NOT NULL DEFAULT CURRENT_TIMESTAMP,
    MODIFICACION     DATETIME      NULL,
    ID_EMPRESA       BIGINT        NOT NULL,
    ID_TEMPORADA     BIGINT        NULL,
    ID_USUARIOI      BIGINT        NOT NULL,
    ID_USUARIOM      BIGINT        NOT NULL,
    INDEX idx_broker  (ID_BROKER),
    INDEX idx_empresa (ID_EMPRESA)
) ENGINE=InnoDB DEFAULT CHARSET=utf8 COLLATE=utf8_general_ci;
