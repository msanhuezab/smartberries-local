CREATE TABLE IF NOT EXISTS liquidacion_detalle_exp (
    ID_DETALLE_EXP BIGINT AUTO_INCREMENT PRIMARY KEY,
    ID_VALOR BIGINT NOT NULL,
    ID_ICARGA BIGINT NOT NULL,
    ID_PRODUCTOR BIGINT NOT NULL,
    ID_VESPECIES BIGINT NOT NULL,
    ID_ESTANDAR BIGINT NOT NULL,
    ID_TCALIBRE BIGINT NOT NULL,
    CANTIDAD_ENVASE INT DEFAULT 0,
    KILOS_NETO DECIMAL(14,2) DEFAULT 0,
    KILOS_BRUTO DECIMAL(14,2) DEFAULT 0,
    FOB_REAL DECIMAL(14,4) DEFAULT 0,
    COSTO_COMISION DECIMAL(14,4) DEFAULT 0,
    COSTO_FLETE DECIMAL(14,4) DEFAULT 0,
    COSTO_OTROS DECIMAL(14,4) DEFAULT 0,
    OBSERVACION VARCHAR(300) NULL,
    ESTADO_REGISTRO TINYINT DEFAULT 1,
    INGRESO DATETIME DEFAULT CURRENT_TIMESTAMP,
    MODIFICACION DATETIME DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
    ID_USUARIOI BIGINT NOT NULL,
    ID_USUARIOM BIGINT NOT NULL,
    UNIQUE KEY uq_liq_exp_linea (ID_VALOR, ID_PRODUCTOR, ID_VESPECIES, ID_ESTANDAR, ID_TCALIBRE),
    KEY idx_liq_exp_icarga (ID_ICARGA),
    KEY idx_liq_exp_valor (ID_VALOR)
);
