CREATE TABLE IF NOT EXISTS fruta_calidad_parametro (
    ID_CALIDAD_PARAMETRO BIGINT(20) NOT NULL AUTO_INCREMENT,
    TIPO_PARAMETRO VARCHAR(30) NOT NULL,
    GRUPO_REPORTE VARCHAR(40) NULL,
    ETAPA VARCHAR(30) NOT NULL,
    CODIGO_PARAMETRO VARCHAR(40) NULL,
    NOMBRE_PARAMETRO VARCHAR(150) NOT NULL,
    UNIDAD_MEDIDA VARCHAR(30) NULL,
    VALOR_MINIMO DECIMAL(12,4) NULL,
    VALOR_MAXIMO DECIMAL(12,4) NULL,
    VALOR_REFERENCIA VARCHAR(150) NULL,
    ES_REQUERIDO TINYINT(1) NOT NULL DEFAULT 0,
    ORDEN INT(11) NOT NULL DEFAULT 0,
    ID_EMPRESA BIGINT(20) NOT NULL,
    ID_TEMPORADA BIGINT(20) NOT NULL,
    ID_ESPECIES BIGINT(20) NOT NULL,
    ID_USUARIOI BIGINT(20) NULL,
    ID_USUARIOM BIGINT(20) NULL,
    INGRESO DATETIME NULL DEFAULT CURRENT_TIMESTAMP,
    MODIFICACION DATETIME NULL DEFAULT NULL ON UPDATE CURRENT_TIMESTAMP,
    ESTADO_REGISTRO INT(11) NOT NULL DEFAULT 1,
    PRIMARY KEY (ID_CALIDAD_PARAMETRO),
    KEY IDX_CALIDAD_PARAMETRO_CONTEXTO (ID_EMPRESA, ID_TEMPORADA, ID_ESPECIES, ETAPA, ESTADO_REGISTRO),
    KEY IDX_CALIDAD_PARAMETRO_GRUPO (GRUPO_REPORTE, ETAPA, ESTADO_REGISTRO),
    KEY IDX_CALIDAD_PARAMETRO_TIPO (TIPO_PARAMETRO, ETAPA, ESTADO_REGISTRO)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4;

CREATE TABLE IF NOT EXISTS fruta_calidad_control (
    ID_CALIDAD_CONTROL BIGINT(20) NOT NULL AUTO_INCREMENT,
    ETAPA VARCHAR(30) NOT NULL,
    MODO_INGRESO VARCHAR(20) NOT NULL,
    TIPO_PRODUCTO VARCHAR(30) NULL,
    ID_OPERACION BIGINT(20) NULL,
    NUMERO_OPERACION VARCHAR(60) NULL,
    FECHA DATE NOT NULL,
    HORA TIME NULL,
    ID_EMPRESA BIGINT(20) NOT NULL,
    ID_PLANTA BIGINT(20) NULL,
    ID_TEMPORADA BIGINT(20) NOT NULL,
    ID_ESPECIES BIGINT(20) NOT NULL,
    ID_USUARIO BIGINT(20) NULL,
    ID_CALIDAD_INSPECTOR BIGINT(20) NULL,
    MUESTRA_GRAMOS DECIMAL(12,4) NULL,
    RESULTADO_GENERAL VARCHAR(30) NULL,
    ESTADO_CONTROL VARCHAR(20) NOT NULL DEFAULT 'ABIERTO',
    PORC_DEFECTO_CONDICION DECIMAL(12,4) NULL,
    PORC_DEFECTO_CALIDAD DECIMAL(12,4) NULL,
    PORC_FIRMEZA DECIMAL(12,4) NULL,
    PORC_ESTIMADO_EXPORTACION DECIMAL(12,4) NULL,
    SCORE_GENERAL DECIMAL(12,4) NULL,
    GRUPO_SCORE VARCHAR(30) NULL,
    OBSERVACION VARCHAR(500) NULL,
    FECHA_CIERRE DATE NULL,
    HORA_CIERRE TIME NULL,
    ID_USUARIO_CIERRE BIGINT(20) NULL,
    OBSERVACION_CIERRE VARCHAR(500) NULL,
    INGRESO DATETIME NULL DEFAULT CURRENT_TIMESTAMP,
    MODIFICACION DATETIME NULL DEFAULT NULL ON UPDATE CURRENT_TIMESTAMP,
    ESTADO_REGISTRO INT(11) NOT NULL DEFAULT 1,
    PRIMARY KEY (ID_CALIDAD_CONTROL),
    KEY IDX_CALIDAD_CONTROL_CONTEXTO (ID_EMPRESA, ID_PLANTA, ID_TEMPORADA, ID_ESPECIES, ETAPA, ESTADO_REGISTRO),
    KEY IDX_CALIDAD_CONTROL_OPERACION (ETAPA, MODO_INGRESO, ID_OPERACION, ESTADO_REGISTRO),
    KEY IDX_CALIDAD_CONTROL_INSPECTOR (ID_CALIDAD_INSPECTOR),
    KEY IDX_CALIDAD_CONTROL_ESTADO (ESTADO_CONTROL, ETAPA, ESTADO_REGISTRO),
    KEY IDX_CALIDAD_CONTROL_FECHA (FECHA, ETAPA, ESTADO_REGISTRO)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4;

CREATE TABLE IF NOT EXISTS fruta_calidad_control_detalle (
    ID_CALIDAD_CONTROL_DETALLE BIGINT(20) NOT NULL AUTO_INCREMENT,
    ID_CALIDAD_CONTROL BIGINT(20) NOT NULL,
    ID_CALIDAD_PARAMETRO BIGINT(20) NULL,
    TIPO_PARAMETRO VARCHAR(30) NOT NULL,
    NOMBRE_PARAMETRO VARCHAR(150) NOT NULL,
    UNIDAD_MEDIDA VARCHAR(30) NULL,
    VALOR_NUMERICO DECIMAL(12,4) NULL,
    VALOR_TEXTO VARCHAR(200) NULL,
    RESULTADO VARCHAR(30) NULL,
    OBSERVACION VARCHAR(300) NULL,
    INGRESO DATETIME NULL DEFAULT CURRENT_TIMESTAMP,
    MODIFICACION DATETIME NULL DEFAULT NULL ON UPDATE CURRENT_TIMESTAMP,
    ESTADO_REGISTRO INT(11) NOT NULL DEFAULT 1,
    PRIMARY KEY (ID_CALIDAD_CONTROL_DETALLE),
    KEY IDX_CALIDAD_DETALLE_CONTROL (ID_CALIDAD_CONTROL, ESTADO_REGISTRO),
    KEY IDX_CALIDAD_DETALLE_PARAMETRO (ID_CALIDAD_PARAMETRO, ESTADO_REGISTRO),
    CONSTRAINT FK_CALIDAD_DETALLE_CONTROL
        FOREIGN KEY (ID_CALIDAD_CONTROL) REFERENCES fruta_calidad_control (ID_CALIDAD_CONTROL)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4;

CREATE TABLE IF NOT EXISTS fruta_calidad_control_folio (
    ID_CALIDAD_CONTROL_FOLIO BIGINT(20) NOT NULL AUTO_INCREMENT,
    ID_CALIDAD_CONTROL BIGINT(20) NOT NULL,
    TIPO_PRODUCTO VARCHAR(30) NOT NULL,
    ID_EXISTENCIA BIGINT(20) NULL,
    FOLIO_ORIGINAL VARCHAR(60) NULL,
    FOLIO_AUXILIAR VARCHAR(60) NULL,
    INGRESO DATETIME NULL DEFAULT CURRENT_TIMESTAMP,
    ESTADO_REGISTRO INT(11) NOT NULL DEFAULT 1,
    PRIMARY KEY (ID_CALIDAD_CONTROL_FOLIO),
    KEY IDX_CALIDAD_FOLIO_CONTROL (ID_CALIDAD_CONTROL, ESTADO_REGISTRO),
    KEY IDX_CALIDAD_FOLIO_BUSQUEDA (TIPO_PRODUCTO, FOLIO_ORIGINAL, FOLIO_AUXILIAR),
    CONSTRAINT FK_CALIDAD_FOLIO_CONTROL
        FOREIGN KEY (ID_CALIDAD_CONTROL) REFERENCES fruta_calidad_control (ID_CALIDAD_CONTROL)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4;
