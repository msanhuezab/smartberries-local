CREATE TABLE IF NOT EXISTS exportadora_ivv (
    ID_IVV BIGINT NOT NULL AUTO_INCREMENT,
    ID_ICARGA BIGINT NOT NULL,
    ID_INVOICE BIGINT DEFAULT NULL,
    ID_VALOR BIGINT DEFAULT NULL,
    NUMERO_IVV INT DEFAULT NULL,
    FECHA_IVV DATE DEFAULT NULL,
    ESTADO_IVV VARCHAR(20) NOT NULL DEFAULT 'BORRADOR',
    TOTAL_INVOICE_USD DECIMAL(14,2) NOT NULL DEFAULT 0,
    TOTAL_LIQUIDACION_USD DECIMAL(14,2) NOT NULL DEFAULT 0,
    DIFERENCIA_USD DECIMAL(14,2) NOT NULL DEFAULT 0,
    DOCUMENTO_SUGERIDO VARCHAR(30) NOT NULL DEFAULT 'SIN_NOTA',
    OBSERVACION_IVV VARCHAR(500) DEFAULT NULL,
    ESTADO_REGISTRO INT DEFAULT 1,
    INGRESO DATETIME DEFAULT NULL,
    MODIFICACION DATETIME DEFAULT NULL,
    ID_EMPRESA BIGINT NOT NULL,
    ID_TEMPORADA BIGINT NOT NULL,
    ID_USUARIOI BIGINT NOT NULL,
    ID_USUARIOM BIGINT NOT NULL,
    PRIMARY KEY (ID_IVV),
    UNIQUE KEY uq_ivv_icarga_activo (ID_ICARGA, ESTADO_REGISTRO),
    KEY idx_ivv_empresa_temporada (ID_EMPRESA, ID_TEMPORADA),
    KEY idx_ivv_estado (ESTADO_IVV)
);
