DROP VIEW IF EXISTS view_recepciones;

CREATE ALGORITHM=UNDEFINED SQL SECURITY INVOKER VIEW view_recepciones AS
SELECT
    'RECEPCION_MP' AS TIPO_REPORTE,
    R.ID_RECEPCION,
    D.ID_DRECEPCION,
    R.NUMERO_RECEPCION AS PROCESO,
