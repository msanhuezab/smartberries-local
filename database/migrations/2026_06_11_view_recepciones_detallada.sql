    CASE
        WHEN R.TRECEPCION = 1 THEN 'Desde Productor '
        WHEN R.TRECEPCION = 2 THEN 'Planta Externa'
        WHEN R.TRECEPCION = 3 THEN 'Desde Productor BDH'
